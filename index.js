function distanceFromHqInBlocks(blockCustomer){
  if (blockCustomer>=42){
   return 42 - blockCustomer ;
  
  }
  else
  return blockCustomer - 42 ; 
}

function distanceFromHqInFeet(blockCustomer){
const distance =  distanceFromHqInFeet(blockCustomer)
return distance*264;
}

